window.t = (key) => {
    const lang = localStorage.getItem("lang") || "vi";
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
};

window.getMovieTitle = (m) => {
    if (!m) return "";
    const lang = localStorage.getItem("lang") || "vi";
    if (lang === "en") {
        // Thử mọi biến thể có thể có của tên gốc
        const original = m.original_title || m.originalTitle || m.original_name || m.name_en;
        if (original) return original;
    }
    return m.title || m.name || "";
};

document.addEventListener("click", (e) => {
    const btn = e.target.closest(".lang-btn");
    if (btn) {
        e.preventDefault();
        const lang = btn.innerText.trim().toUpperCase() === "EN" ? "en" : "vi";
        setLanguage(lang);
    }
});


document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("lang") || "vi";
    setLanguage(savedLang);
});


