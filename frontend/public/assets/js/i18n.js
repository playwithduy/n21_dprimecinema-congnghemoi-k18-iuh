function setLanguage(lang) {
    if (!translations[lang]) lang = "vi";
    localStorage.setItem("lang", lang);
    
    // 2. Dịch các phần tử data-i18n
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else if (el.hasAttribute('title')) {
                el.title = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });

    // 3. Dịch các phần tử động (Movie Titles)
    document.querySelectorAll("[data-movie-title]").forEach(el => {
        const title = el.getAttribute("data-movie-title");
        const original = el.getAttribute("data-movie-original");
        if (title && original) {
            el.innerText = (lang === 'en') ? original : title;
        }
    });

    // Update buttons UI
    document.querySelectorAll(".lang-btn").forEach(btn => {
        const btnText = btn.innerText.trim().toUpperCase();
        const isTarget = (lang === "vi" && (btnText === "VN" || btnText === "VI")) || 
                         (lang === "en" && btnText === "EN");
        btn.classList.toggle("active", isTarget);
    });
    
    document.documentElement.lang = lang;
    document.documentElement.classList.remove('lang-en-loading');
    window.dispatchEvent(new CustomEvent("langChanged", { detail: lang }));
}


